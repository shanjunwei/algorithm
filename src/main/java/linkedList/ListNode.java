package linkedList;

public class ListNode {
    int val;
    ListNode next;

    public ListNode() {
    }

    public ListNode(int data) {
        this.val = data;
    }

    public int getData() {
        return val;
    }

    public void setData(int data) {
        this.val = data;
    }

    public ListNode getNext() {
        return next;
    }

    public void setNext(ListNode next) {
        this.next = next;
    }

    public int getVal() {
        return val;
    }

    public void setVal(int val) {
        this.val = val;
    }
}
